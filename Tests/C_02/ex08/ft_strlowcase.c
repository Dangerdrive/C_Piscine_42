char	*ft_strlowcase(char *dest, char *src)
{
	while (*src != '\0')
	{
		if (*src >= 'A' && *src <= 'Z')
		{
		*dest = *src + 32;
		}
		else
		{
			*dest = *src;
		}
		dest++;
		src++;
	}
	*dest = '\0';
	return (dest);
}
