char	*ft_strupcase(char *dest, char *src)
{
	while (*src != '\0')
	{
		if (*src >= 'a' && *src <= 'z')
		{
		*dest = *src - 32;
		}
		else
		{
			*dest = *src;
		}
		dest++;
		src++;
	}
	*dest = '\0';
	return (dest);
}
